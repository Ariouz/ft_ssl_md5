#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	r;

	i = 0;
	r = ft_strlen((char *)src);
	if (!dst || !src)
		return (0);
	if (size == 0)
		return (r);
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (r);
}
