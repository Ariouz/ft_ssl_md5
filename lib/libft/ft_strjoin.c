#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	int		total_len;
	char	*dst;

	i = 0;
	j = 0;
	total_len = ft_strlen((char *)s1) + ft_strlen((char *)s2);
	dst = malloc(sizeof(char) * total_len + 1);
	if (!dst)
		return (NULL);
	while (s1[i])
	{
		dst[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		dst[i + j] = s2[j];
		j++;
	}
	dst[i + j] = 0;
	return (dst);
}
